package _package1;

public class ConditionalDemo {

    public static void main(String[] args){
        int val1 = 1;
        int val2 = 2;
        if((val1 == 1) && (val2 == 2))
            System.out.println("val1 is 1 and val2 is 2");
        if((val1 == 1) || (val2 == 2))
            System.out.println("val1 is 1 or val2 is 2");

    }
}
